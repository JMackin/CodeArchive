import math

x = -1.5

print("ceiling, floor, absolute value")
print(math.ceil(x))
print(math.floor(x))
print(math.fabs(x))

print("\nSum")
print(math.fsum([0.1,0.5,5.1,4.2]))

print("\nLog, LN")
print(math.log(math.e))
print(math.log(math.e,10))
print(math.log10(math.e))

print("\nPower, Sqrt")
print(math.pow(2,5))
print(math.sqrt(144))

print("\ncos, sin, tan")
print(math.cos(math.pi))
print(math.sin(math.pi))
print(math.tan(math.pi))
print(math.degrees(math.pi))
print(math.radians(180))